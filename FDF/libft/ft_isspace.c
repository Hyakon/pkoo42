/*
 ** Description : Check if c is a blank charcacter
 ** Return : True or False
*/

int		ft_isspace(char c)
{
	if (c == '\t' || c == '\v' || c == '\n' || c == '\r' ||
			c == '\f' || c == ' ')
		return (42);
	return (0);
}
